// Renders the Supabase Auth templates from @repo/email-templates into plain HTML.
//
// Supabase can't import TypeScript: the CLI reads an HTML file and the hosted
// dashboard wants pasted markup. So the package stays the single source of
// truth and this emits the artifact, rather than the HTML being maintained by
// hand in two places and drifting.
//
// Run: pnpm --filter @repo/email-templates render:supabase

import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import {
  generateAuthConfirmationEmail,
  AUTH_CONFIRMATION_SUBJECT,
} from '../dist/index.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const outDir = join(here, '..', '..', 'supabase', 'templates');

// Go template placeholders Supabase substitutes at send time — passed straight
// through as if they were values, so the same function serves real sends too.
const TEMPLATES = [
  {
    file: 'confirmation.html',
    subject: AUTH_CONFIRMATION_SUBJECT,
    html: generateAuthConfirmationEmail({
      confirmationUrl: '{{ .ConfirmationURL }}',
      email: '{{ .Email }}',
    }),
  },
];

mkdirSync(outDir, { recursive: true });

const banner = '<!-- GENERATED by @repo/email-templates. Edit the package, then rerun render:supabase. -->\n';

for (const { file, subject, html } of TEMPLATES) {
  writeFileSync(join(outDir, file), banner + html + '\n', 'utf8');
  console.log(`wrote ${file}  (subject: ${subject})`);
}
